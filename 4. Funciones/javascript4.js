// Funciones
    //Declaración
    function prueba () {
        console.log("esta es una función");
    }
    //Llamado de la función
    prueba();


// Parametros : declaración de valores que se pueden pasar al llamar la función
function prueba2 ( parametro1, parametro2 ) {
    console.log(`Estos son los parametros: ${parametro1} y ${parametro2}` );
}

//Argumentos: valores que se le pasan eal llamar una función
prueba2("un numero", "unbooleano")